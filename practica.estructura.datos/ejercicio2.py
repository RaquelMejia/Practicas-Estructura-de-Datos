#Ingrese dos números y visualizar el número mayor y número menor.
num1 = int(input("Ingresa el primer número: "))
num2 = int(input("Ingresa el segundo número: "))

if num1 > num2:
    print (num1 ," es mayor")
    print (num2, " es menor")

else:
    print (num2 ,"es mayor")
    print (num1, "es menor")